gameState = "menu"

function draw_map()
   for y=1, map_display_h do
      for x=1, map_display_w do                                                         
         love.graphics.draw( 
            tile[map[y+map_y][x+map_x]], 
            (x*tile_w)+map_offset_x, 
            (y*tile_h)+map_offset_y )
      end
   end
end

function testMap(x, y)
    --if the tile is rock or water
    --I add an extra one due to the way lua handles table indexing
    if map[(player.grid_y / 16) + 1 + y][(player.grid_x / 16) + 1 + x] == 2 or map[(player.grid_y / 16) + 1 + y][(player.grid_x / 16) + 1 + x] == 3 then
        return false
    end
    return true
end

