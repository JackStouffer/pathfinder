function Interpolate(float x0, float x1, float alpha)
    --linear interpolation
    return x0 * (1 - alpha) + alpha * x1;
end

function GenerateWhiteNoise(width, height)
    noise = {}
    for x=1,width do
        noise[x] = {}
        for y=1,height do
            noise[x][y] = math.random()
        end
    end
    return noise
end

function GenerateSmoothNoise(baseNoise, octave)
{
    width = getn(baseNoise)
    height = getn(baseNoise[1])
    smoothnoise = {}
    for x=1,width do
        noise[x] = {}
        for y=1,height do
            noise[x][y] = 0
        end
    end
 
   samplePeriod = 2 ^ octave 
   sampleFrequency = 1.0 / samplePeriod
 
   for i = 1, width do
        --calculate the horizontal sampling indices
        sample_i0 = (i / samplePeriod) * samplePeriod;
        sample_i1 = (sample_i0 + samplePeriod) % width; --wrap around
        horizontal_blend = (i - sample_i0) * sampleFrequency;
 
        for j = 1, height do
            --calculate the vertical sampling indices
            sample_j0 = (j / samplePeriod) * samplePeriod;
            sample_j1 = (sample_j0 + samplePeriod) % height; --wrap around
            vertical_blend = (j - sample_j0) * sampleFrequency;
 
            --blend the top two corners
            top = Interpolate(baseNoise[sample_i0][sample_j0], baseNoise[sample_i1][sample_j0], horizontal_blend);
 
            --blend the bottom two corners
            bottom = Interpolate(baseNoise[sample_i0][sample_j1], baseNoise[sample_i1][sample_j1], horizontal_blend);
 
            --final blend
            smoothNoise[i][j] = Interpolate(top, bottom, vertical_blend);
        end
   end
 
   return smoothNoise;
end
